package EstruturasDeControle;

public class WhileDeterminado {

    public static void main(String[] args) {
        /*Enquanto uma função for verdadeira, o while vai repetir até dar falso*/
              int contador = 0;

                while (contador <= 10){
                    System.out.println(contador);
                    contador+= 2;

                }

    }
}
